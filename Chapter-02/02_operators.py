# Arithmetic Operators
print('The value of 4+2 is', 4+2)
print('The value of 4-2 is', 4-2)
print('The value of 4*2 is', 4*2)
print('The value of 4/2 is', 4/2)

# Comparison Operators
print(7>2)
print(7<2)
print(7==2)
print(7!=2)
print(7<=2)
print(7>=2)

# Assignment Operator
a = 42
a += 14
a -= 14
a *= 14
a /= 14
print(a)

# Logical Operators
b = True
c = False
print('The value of b and c is',b and c) #If Both the variables are True in other cases False
print('The value of b or c is',b or c) #If any one of the variable is True then it will return True
print('The value of not c is',not c) #It will give opposite eg. If True then it will give False & If False then it will give True 

